public class PalabrasClave {

	/*
	 * Palabras clave o reservadas en Java: Son palabras reservadas utilizadas
	 * por el lenguaje de programación Java y, por tanto, no pueden utilizarse como
	 * identificadores de variables, objetos o métodos.
	 */

	// Listado de palabras clave:
	/*
	 * abstract assert** boolean break byte case catch char class continue default
	 * do double else enum*** extends final finally float for if implements import
	 * instanceof int interface long native new package private protected public
	 * return short static strictfp* super switch synchronized this throw throws
	 * transient try void volatile while
	 */
	/*
	 * Las palabras clave const y goto, aunque no se utilizan actualmente, tampoco
	 * se pueden utilizar como identificadores, al igual que los literales true,
	 * false y null. Tomado de:
	 * https://masqueprogramar.wordpress.com/2015/12/19/palabras-reservadas-en-java/
	 * #:~:text=Las%20palabras%20clave%20de%20Java%20%28%20Java%20Language,
	 * actualmente%2C%20tampoco%20se%20pueden%20utilizar%20como%20identificadores%2C
	 * %20
	 */
}
