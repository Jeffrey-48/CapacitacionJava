package java_collections_and_data_structure;

public class MetodosList {
	
	/*
	 * Metodos:
	 * 
	 * sise(); indica el tamaño de la lista
	 * isEmpy(); indica si la lista esta vacia o no
	 * contains(object) indica si el objeto esta en la lista
	 * add(element) agrega el elemento a la lista
	 * remove(object) elimina el objeto de la lista
	 * containsAll(list) indica si la lista esta contenida en la lista inicial
	 * addAll(list) agrega la lista completa a la lista original
	 * addAll(index,list) agrega la lista en una posicion especifica
	 * removeAll(lista) elimina la lista 
	 * clear() limpiar completamente la lista
	 * equals(object) identifica si el objeto es igual a algun elemento de la lista
	 * get(index) obtiene el elemento en una posicion
	 * set(object) camibia el elemento de una posicion por otro elemento
	 * set(index,object) camibia el elemento de una posicion por otro elemento
	 * add(index,oject) agrega el elemento en una posicion
	 * remove(index) eliminar el elemento de la posicion
	 * indexOf(object) retorna la posicion donde esta el objeto
	 * lastIndexOf(object) retorna la ultima posicion donde esta el objeto
	 * copyOf(list) hace una copia de la lista
	 */
}
